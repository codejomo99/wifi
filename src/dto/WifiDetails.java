package dto;


public class WifiDetails {
    private String wifiName;
    private String mgrNo;
    private String address;
    private String installationType;
    private String installationYear;
    private String installationAgency;
    private String latitude;
    private String longitude;
    private String floor;
    private String serviceType;

    // Getters and Setters
    public String getWifiName() {
        return wifiName;
    }

    public void setWifiName(String wifiName) {
        this.wifiName = wifiName;
    }

    public String getMgrNo() {
        return mgrNo;
    }

    public void setMgrNo(String mgrNo) {
        this.mgrNo = mgrNo;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getInstallationType() {
        return installationType;
    }

    public void setInstallationType(String installationType) {
        this.installationType = installationType;
    }

    public String getInstallationYear() {
        return installationYear;
    }

    public void setInstallationYear(String installationYear) {
        this.installationYear = installationYear;
    }

    public String getInstallationAgency() {
        return installationAgency;
    }

    public void setInstallationAgency(String installationAgency) {
        this.installationAgency = installationAgency;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getFloor() {
        return floor;
    }

    public void setFloor(String floor) {
        this.floor = floor;
    }

    public String getServiceType() {
        return serviceType;
    }

    public void setServiceType(String serviceType) {
        this.serviceType = serviceType;
    }
}
